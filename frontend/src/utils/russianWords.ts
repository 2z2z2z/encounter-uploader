/**
 * Возвращает случайное русское слово
 * @param length Требуемая длина слова (опционально)
 * @returns Случайное русское слово
 */
export function getRandomWord(length?: number): string {
  if (length) {
    const filtered = RUSSIAN_WORDS.filter(word => word.length === length)
    if (filtered.length === 0) {
      const closest = RUSSIAN_WORDS.reduce((prev, curr) =>
        Math.abs(curr.length - length) < Math.abs(prev.length - length) ? curr : prev
      )
      return closest
    }
    return filtered[Math.floor(Math.random() * filtered.length)]
  }

  return RUSSIAN_WORDS[Math.floor(Math.random() * RUSSIAN_WORDS.length)]
}

/**
 * Возвращает случайную русскую букву (А-Я)
 */
export function getRandomCyrillicLetter(): string {
  const letters = 'АБВГДЕЖЗИЙКЛМНОПРСТУФХЦЧШЩЭЮЯ'
  return letters[Math.floor(Math.random() * letters.length)]
}

/**
 * Возвращает случайную английскую букву (A-Z)
 */
export function getRandomLatinLetter(): string {
  return String.fromCharCode(65 + Math.floor(Math.random() * 26))
}

/**
 * Возвращает случайную цифру (0-9)
 */
export function getRandomDigit(): string {
  return String(Math.floor(Math.random() * 10))
}